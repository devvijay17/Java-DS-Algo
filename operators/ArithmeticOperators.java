package operators;

public class ArithmeticOperators {
	
	public static void main(String[] args) {
		
		int a = 23;
		int b = 40;
		int c = a+b;
		int d = a-b;
		int e = a*b;
		int f = b/a;
		int g = a%b;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		
		
	}
}
